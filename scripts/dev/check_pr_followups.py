#!/usr/bin/env python3
"""Check that declared deferred PR work has an explicit follow-up disposition.

The check is intentionally bounded for agent PR-readiness runs.  It reads a PR
body from an explicit file or GitHub pull-request event payload, scans the
default template's Follow-Up Issues section, and prints a compact report.
"""

from __future__ import annotations

import argparse
import json
import os
import posixpath
import re
import sys
from dataclasses import dataclass
from pathlib import Path

ISSUE_RE = re.compile(r"(?:#|/issues/)(\d+)\b")
HEADING_RE = re.compile(r"^(#{1,6})\s+(.+?)\s*$", re.MULTILINE)
HTML_COMMENT_RE = re.compile(r"<!--.*?-->", re.DOTALL)
AUTOGENERATED_BLOCK_RE = re.compile(
    r"<!--\s*This is an auto-generated comment:\s*(?P<kind>.*?)\s*-->"
    r".*?"
    r"<!--\s*end of auto-generated comment:\s*(?P=kind)\s*-->",
    re.DOTALL | re.IGNORECASE,
)
CLOSING_KEYWORD_RE = re.compile(
    r"\b(?:close[sd]?|fix(?:e[sd])?|resolve[sd]?)\s+`?(?:#\d+|https?://\S+/issues/\d+)",
    re.IGNORECASE,
)
RESIDUAL_SCOPE_RE = re.compile(
    r"\b("
    r"partial(?:ly)?|diagnostic[- ]only|blocked|degraded|residual scope|remaining work|"
    r"still (?:needs|requires|missing)|deferred work|follow[- ]up (?:needed|required)|"
    r"not (?:benchmark|paper)[- ](?:strength|facing|evidence)"
    r")\b",
    re.IGNORECASE,
)
WAIVER_RE = re.compile(r"\b(?:maintainer\s+)?waiver\b", re.IGNORECASE)
EMPTY_OR_PLACEHOLDER = {
    "",
    "-",
    "n/a",
    "na",
    "none",
    "no",
    "not applicable",
    "`#<id>`",
    "#<id>",
    "<id>",
}
DOMAIN_APPROVAL_NOT_REQUIRED = {
    "docs-only",
    "docs only",
    "n/a",
    "na",
    "not applicable",
}
DOMAIN_VALIDITY_LABELS = (
    "Target claim/hypothesis",
    "Comparator or split/evidence validity",
    "Fallback/degraded exclusions",
    "Claim boundary",
    "Implementation integrity vs experimental validity",
)
FREEFORM_DOMAIN_TRIGGER_RE = re.compile(
    r"\b("
    r"evidence[_ -]?tier\s*:|claim[_ -]?boundary\s*:|diagnostic[- ]only|"
    r"not\s+paper[- ]grade|benchmark interpretation|paper[- ]facing claim"
    r")\b",
    re.IGNORECASE,
)
# A trigger phrase is ignored when it is negated in prose (e.g. "makes no paper-facing claim"),
# because describing the absence of a claim is the opposite of making one. Without this, honestly
# stating an evidence boundary in a PR body would (perversely) require domain approval.
_NEGATED_TRIGGER_RE = re.compile(r"\b(no|not|without|never|non)\b[\s\w-]{0,24}$", re.IGNORECASE)
# Free-form triggers are prose *mentions* of an evidence concept (e.g. a docs page that discusses
# "benchmark interpretation"), a weaker signal than a filled Research Result Guidance declaration.
# Only free-form triggers may be opted out of with the documented docs-only/not-required branch.
_FREEFORM_TRIGGER_PREFIX = "free-form evidence marker: "
BOT_ONLY_MARKERS = (
    "auto-generated comment: release notes by coderabbit.ai",
    "auto-generated comment: summarize by coderabbit.ai",
    "summary by coderabbit",
    "generated with [claude code]",
)
NON_SUBSTANTIVE_PREFIXES = (
    "docs/",
    ".github/PULL_REQUEST_TEMPLATE/",
)
NON_SUBSTANTIVE_SUFFIXES = (
    ".md",
    ".rst",
    ".txt",
)


@dataclass(frozen=True)
class FollowupReport:
    """Compact PR follow-up disposition report."""

    status: str
    source: str
    deferred_work: str
    linked_issues: tuple[str, ...]
    explicit_no_issue_reason: str
    issue_state_errors: tuple[str, ...]
    message: str


@dataclass(frozen=True)
class DomainApprovalReport:
    """Compact domain-aware approval report for evidence-validity-sensitive PRs."""

    status: str
    source: str
    sensitive_terms: tuple[str, ...]
    required: str
    approval_status: str
    approval_note: str
    checklist_errors: tuple[str, ...]
    message: str


@dataclass(frozen=True)
class BodyQualityReport:
    """Compact report for PR body presence on substantive changes."""

    status: str
    source: str
    substantive_files: tuple[str, ...]
    message: str


def _normalize_label(text: str) -> str:
    return re.sub(r"[^a-z0-9]+", " ", text.lower()).strip()


def _strip_bullet_prefix(text: str) -> str:
    return re.sub(r"^\s*(?:[-*+]|\d+[.)])\s*", "", text).strip()


def _clean_value(text: str) -> str:
    return _strip_bullet_prefix(text).strip().strip("`").strip()


def _clean_body_for_quality(body: str) -> str:
    """Return body text with comments and whitespace removed for quality checks."""
    without_generated_blocks = AUTOGENERATED_BLOCK_RE.sub("", body)
    return HTML_COMMENT_RE.sub("", without_generated_blocks).strip()


def _is_bot_only_body(body: str) -> bool:
    """Return whether the body appears to contain only generated bot release notes."""
    cleaned = _clean_body_for_quality(body)
    if not cleaned:
        lowered_body = body.lower()
        return any(marker in lowered_body for marker in BOT_ONLY_MARKERS)
    lowered = cleaned.lower()
    if not any(marker in lowered for marker in BOT_ONLY_MARKERS):
        return False
    without_bot_markers = lowered
    for marker in BOT_ONLY_MARKERS:
        without_bot_markers = without_bot_markers.replace(marker, "")
    without_markdown_noise = re.sub(r"[*_`>#\-\s:|]+", "", without_bot_markers)
    return not without_markdown_noise or cleaned.startswith("## Summary by CodeRabbit")


def _is_substantive_file(path: str) -> bool:
    """Return whether a changed file should require a human PR description."""
    raw_path = path.strip()
    if not raw_path:
        return False
    normalized = posixpath.normpath(raw_path.replace("\\", "/"))
    if any(
        normalized == prefix.rstrip("/") or normalized.startswith(prefix)
        for prefix in NON_SUBSTANTIVE_PREFIXES
    ):
        return False
    return not any(normalized.endswith(suffix) for suffix in NON_SUBSTANTIVE_SUFFIXES)


def _read_changed_files(path: Path | None) -> tuple[str, ...]:
    if path is None:
        return ()
    return tuple(
        line.strip() for line in path.read_text(encoding="utf-8").splitlines() if line.strip()
    )


def _is_empty_or_placeholder(text: str) -> bool:
    value = _clean_value(text).lower()
    return value in EMPTY_OR_PLACEHOLDER or value.startswith("#<")


def _is_option_placeholder(text: str) -> bool:
    """Return whether a PR template option list was left unselected."""
    return " / " in _clean_value(text).lower()


def _is_empty_or_option_placeholder(text: str) -> bool:
    return _is_empty_or_placeholder(text) or _is_option_placeholder(text)


def _is_no_issue_reason(text: str) -> bool:
    value = _clean_value(text).lower()
    prefixes = ("none", "no", "n/a", "na", "not applicable")
    for prefix in prefixes:
        if value.startswith(f"{prefix} - ") or value.startswith(f"{prefix}: "):
            return bool(value.removeprefix(prefix).lstrip(" -:").strip())
    return False


def _remove_section(body: str, heading: str) -> str:
    """Return body text with a named Markdown section removed."""
    target = _normalize_label(heading)
    matches = list(HEADING_RE.finditer(body))
    for index, match in enumerate(matches):
        level, title = match.groups()
        if _normalize_label(title) != target:
            continue
        section_end = len(body)
        for next_match in matches[index + 1 :]:
            if len(next_match.group(1)) <= len(level):
                section_end = next_match.start()
                break
        return f"{body[: match.start()]}\n{body[section_end:]}"
    return body


def _has_closing_keyword(body: str) -> bool:
    """Return true when a PR body declares issue-closing intent."""
    return CLOSING_KEYWORD_RE.search(body) is not None


def _has_residual_scope_outside_followups(body: str) -> bool:
    """Return true when body text outside Follow-Up Issues declares residual scope."""
    return RESIDUAL_SCOPE_RE.search(_remove_section(body, "Follow-Up Issues")) is not None


def _has_explicit_maintainer_waiver(body: str, issue_value: str) -> bool:
    """Return true when residual closure is explicitly waived."""
    return WAIVER_RE.search(f"{body}\n{issue_value}") is not None


def _extract_section(body: str, heading: str) -> str:
    target = _normalize_label(heading)
    matches = list(HEADING_RE.finditer(body))
    for index, match in enumerate(matches):
        level, title = match.groups()
        if _normalize_label(title) != target:
            continue
        section_end = len(body)
        for next_match in matches[index + 1 :]:
            if len(next_match.group(1)) <= len(level):
                section_end = next_match.start()
                break
        return body[match.end() : section_end].strip()
    return ""


def _is_continuation_boundary(line: str, *, parent_indent: int, known_labels: set[str]) -> bool:
    """Return whether *line* starts the next top-level field or section."""
    stripped = line.strip()
    indent = len(line) - len(line.lstrip())
    if indent <= parent_indent and line.lstrip().startswith(("-", "*", "+")):
        return True
    clean = _strip_bullet_prefix(stripped)
    if ":" in clean and _normalize_label(clean.split(":", 1)[0]) in known_labels:
        return True
    return stripped.startswith("#")


def _value_after_label(section: str, label: str) -> str:
    target = _normalize_label(label)
    lines = section.splitlines()
    known_labels = {
        "deferred work",
        "issues opened for follow up",
        "issues opened for followup",
        "follow up issues",
        "required for this pr",
        "domains reviewed",
        "status",
        "approver review source or waiver",
        "approval or blocker note",
        "validity checklist",
        *(_normalize_label(label) for label in DOMAIN_VALIDITY_LABELS),
    }
    for index, line in enumerate(lines):
        item = _strip_bullet_prefix(line)
        if ":" not in item:
            continue
        key, value = item.split(":", 1)
        if _normalize_label(key) != target:
            continue
        value = value.strip()
        if value:
            return value
        continuation: list[str] = []
        parent_indent = len(line) - len(line.lstrip())
        for following in lines[index + 1 :]:
            stripped = following.strip()
            if not stripped:
                continue
            if _is_continuation_boundary(
                following,
                parent_indent=parent_indent,
                known_labels=known_labels,
            ):
                break
            clean = _strip_bullet_prefix(stripped)
            continuation.append(clean)
        return " ".join(continuation).strip()
    return ""


def _linked_issues(text: str) -> tuple[str, ...]:
    return tuple(f"#{match}" for match in sorted(set(ISSUE_RE.findall(text)), key=int))


def _domain_value_not_required(text: str) -> bool:
    """Return whether a domain-approval trigger field explicitly opts out."""
    value = _clean_value(text).lower()
    if _is_option_placeholder(text):
        return False
    if value in DOMAIN_APPROVAL_NOT_REQUIRED or value in EMPTY_OR_PLACEHOLDER:
        return True
    return any(_has_value_prefix(value, prefix) for prefix in DOMAIN_APPROVAL_NOT_REQUIRED)


def _domain_required_opts_out(required: str) -> bool:
    """Return whether the 'Required for this PR' field explicitly opts out with a reason.

    The PR template documents ``Required for this PR: no - reason`` as the docs-only branch. A bare
    empty value or an unselected ``yes / no`` option list is *not* a deliberate opt-out, so those
    stay on the strict path that demands an explicit answer.
    """
    if _is_empty_or_option_placeholder(required):
        return False
    value = _clean_value(required).lower()
    return any(
        _has_value_prefix(value, prefix)
        for prefix in ("no", "n/a", "na", "not applicable", "docs-only", "docs only")
    )


def _domain_status_opts_out(status_value: str) -> bool:
    """Return whether an approval status explicitly selects the not-required/waived branch."""
    if re.search(r"\bnot\s+required\b", status_value) or re.search(r"\bwaiv", status_value):
        return True
    return status_value in {"na", "n/a", "not applicable", "none"}


def _domain_triggers_are_freeform_only(sensitive_terms: tuple[str, ...]) -> bool:
    """Return whether every trigger is a weak free-form prose mention (never a structured field).

    Structured Research Result Guidance declarations (a filled ``Evidence tier`` / ``Result
    classification``) are a strong self-declaration and must keep the strict approval path. Only
    prose *mentions* of an evidence concept are eligible for the docs-only opt-out.
    """
    return bool(sensitive_terms) and all(
        term.startswith(_FREEFORM_TRIGGER_PREFIX) for term in sensitive_terms
    )


def _is_docs_only_domain_opt_out(
    sensitive_terms: tuple[str, ...], required: str, status_value: str
) -> bool:
    """Return whether the documented docs-only / not-required approval opt-out applies.

    True only when every trigger is a weak free-form prose mention, the ``Required for this PR``
    field explicitly opts out with a reason, and the ``Status`` selects the not-required/waived
    branch. This keeps structured Research Result Guidance declarations on the strict path.
    """
    return (
        _domain_triggers_are_freeform_only(sensitive_terms)
        and _domain_required_opts_out(required)
        and _domain_status_opts_out(status_value)
    )


def _has_value_prefix(value: str, prefix: str) -> bool:
    """Return whether *value* starts with *prefix* as a standalone token."""
    if not value.startswith(prefix):
        return False
    rest = value[len(prefix) :]
    return not rest or rest[0] in " -:,()"


def _domain_approval_triggers(body: str) -> tuple[str, ...]:
    """Return template-field triggers that require domain-aware approval."""
    section = _extract_section(body, "Research Result Guidance")
    if not section:
        return tuple(
            f"{_FREEFORM_TRIGGER_PREFIX}{match.group(1)}"
            for match in FREEFORM_DOMAIN_TRIGGER_RE.finditer(body)
            if not _NEGATED_TRIGGER_RE.search(body[: match.start()])
        )
    triggers: list[str] = []
    for label in ("Evidence tier", "Result classification"):
        value = _value_after_label(section, label)
        if value and not _domain_value_not_required(value):
            triggers.append(f"{label}: {value}")
    return tuple(triggers)


def _domain_checklist_errors(section: str) -> tuple[str, ...]:
    """Return missing or placeholder validity-checklist fields."""
    errors: list[str] = []
    for label in DOMAIN_VALIDITY_LABELS:
        value = _value_after_label(section, label)
        cleaned = _clean_value(value)
        if (
            not cleaned
            or cleaned == "-"
            or cleaned.startswith("#<")
            or _is_option_placeholder(value)
        ):
            errors.append(label)
    return tuple(errors)


def _verify_open_issues(issues: tuple[str, ...]) -> tuple[str, ...]:
    from scripts.dev.gh_issue_rest import fetch_issue

    errors: list[str] = []
    for issue in issues:
        number = issue.removeprefix("#")
        try:
            issue_number = int(number)
        except ValueError:
            errors.append(f"{issue}: unable to verify open state (invalid issue number)")
            continue
        payload = fetch_issue(issue_number)
        if payload.get("status") != "ok":
            detail = payload.get("error", "unknown error")
            errors.append(f"{issue}: unable to verify open state ({detail})")
            continue
        state = payload.get("state", "").upper()
        if state != "OPEN":
            errors.append(f"{issue}: state is {state or 'unknown'}, expected OPEN")
    return tuple(errors)


def analyze_body(body: str, *, source: str, require_open_issues: bool = False) -> FollowupReport:
    """Return a compact follow-up report for a PR body."""
    section = _extract_section(body, "Follow-Up Issues")
    deferred = _value_after_label(section, "Deferred work") if section else ""
    issue_value = _value_after_label(section, "Issues opened for follow-up") if section else ""
    linked_issues = _linked_issues(f"{section}\n{issue_value}")
    issue_state_errors = _verify_open_issues(linked_issues) if require_open_issues else ()

    residual_closure = _has_closing_keyword(body) and _has_residual_scope_outside_followups(body)
    if not section:
        # The section is only required when there is residual scope to track (the PR closes an issue
        # yet declares out-of-scope follow-up work). A self-contained PR with no deferred work does
        # not need to carry a boilerplate "Follow-Up Issues: none" section.
        if residual_closure and not _has_explicit_maintainer_waiver(body, ""):
            return FollowupReport(
                status="residual_scope_without_followup",
                source=source,
                deferred_work="",
                linked_issues=(),
                explicit_no_issue_reason="",
                issue_state_errors=(),
                message=(
                    "PR closes an issue while declaring residual scope but has no Follow-Up Issues "
                    "section; add the section with a linked open follow-up issue or a maintainer "
                    "waiver."
                ),
            )
        return FollowupReport(
            status="ok",
            source=source,
            deferred_work="",
            linked_issues=(),
            explicit_no_issue_reason="",
            issue_state_errors=(),
            message="No Follow-Up Issues section required (no residual scope declared).",
        )
    if _is_empty_or_placeholder(deferred):
        if (
            residual_closure
            and not linked_issues
            and not _has_explicit_maintainer_waiver(body, issue_value)
        ):
            return FollowupReport(
                status="residual_scope_without_followup",
                source=source,
                deferred_work="",
                linked_issues=(),
                explicit_no_issue_reason="",
                issue_state_errors=(),
                message=(
                    "PR closes an issue while declaring residual scope outside Follow-Up Issues; "
                    "link an open follow-up issue or add an explicit maintainer waiver."
                ),
            )
        return FollowupReport(
            status="ok",
            source=source,
            deferred_work="",
            linked_issues=linked_issues,
            explicit_no_issue_reason="",
            issue_state_errors=issue_state_errors,
            message="No deferred work declared.",
        )
    if linked_issues:
        if issue_state_errors:
            return FollowupReport(
                status="issue_state_error",
                source=source,
                deferred_work=deferred,
                linked_issues=linked_issues,
                explicit_no_issue_reason="",
                issue_state_errors=issue_state_errors,
                message="Deferred work follow-up issue state could not be verified open.",
            )
        return FollowupReport(
            status="ok",
            source=source,
            deferred_work=deferred,
            linked_issues=linked_issues,
            explicit_no_issue_reason="",
            issue_state_errors=(),
            message="Deferred work has linked follow-up issue(s).",
        )
    if residual_closure and _has_explicit_maintainer_waiver(body, issue_value):
        return FollowupReport(
            status="ok",
            source=source,
            deferred_work=deferred,
            linked_issues=(),
            explicit_no_issue_reason=issue_value,
            issue_state_errors=(),
            message="Residual scope is covered by an explicit maintainer waiver.",
        )
    if _is_no_issue_reason(issue_value):
        return FollowupReport(
            status="ok",
            source=source,
            deferred_work=deferred,
            linked_issues=(),
            explicit_no_issue_reason=issue_value,
            issue_state_errors=(),
            message="Deferred work explicitly states no follow-up issue is needed.",
        )
    return FollowupReport(
        status="missing_followup",
        source=source,
        deferred_work=deferred,
        linked_issues=(),
        explicit_no_issue_reason="",
        issue_state_errors=(),
        message="Deferred work is declared without a linked issue or explicit NA/none reason.",
    )


def _domain_status_report(
    status_value: str,
    *,
    source: str,
    sensitive_terms: tuple[str, ...],
    required: str,
    approval_status: str,
    approval_note: str,
    checklist_errors: tuple[str, ...],
) -> DomainApprovalReport | None:
    """Return a report when the approval status is not a clean approved/waived value, else None."""
    unapproved = re.search(r"\b(blocked?|blocker|no|not|pending|awaiting)\b", status_value)
    if unapproved or not re.search(r"\b(approved?|waived|waiver)\b", status_value):
        pending = bool(re.search(r"\b(blocked?|blocker|pending|awaiting)\b", status_value))
        return DomainApprovalReport(
            status="pending_domain_approval" if pending else "invalid_domain_approval_status",
            source=source,
            sensitive_terms=sensitive_terms,
            required=required,
            approval_status=approval_status,
            approval_note=approval_note,
            checklist_errors=checklist_errors,
            message=(
                "Domain-aware approval status must say approved or waived before final readiness."
            ),
        )
    return None


def analyze_domain_approval(body: str, *, source: str) -> DomainApprovalReport:
    """Return whether domain-sensitive PR bodies carry explicit approval or blocker status."""
    sensitive_terms = _domain_approval_triggers(body)
    if not sensitive_terms:
        return DomainApprovalReport(
            status="ok",
            source=source,
            sensitive_terms=(),
            required="",
            approval_status="",
            approval_note="",
            checklist_errors=(),
            message="No domain-sensitive evidence-validity trigger found.",
        )

    section = _extract_section(body, "Domain-Aware Approval")
    if not section:
        return DomainApprovalReport(
            status="missing_domain_approval",
            source=source,
            sensitive_terms=sensitive_terms,
            required="",
            approval_status="",
            approval_note="",
            checklist_errors=(),
            message=(
                "Domain-sensitive evidence-validity terms are present; add a "
                "Domain-Aware Approval section with approval, waiver, or blocker details."
            ),
        )

    required = _value_after_label(section, "Required for this PR")
    if not required:
        required = _value_after_label(section, "Required for PR")
    domains = _value_after_label(section, "Domains reviewed")
    approval_status = _value_after_label(section, "Status")
    approval_note = _value_after_label(section, "Approver/review source or waiver")
    if not approval_note:
        approval_note = _value_after_label(section, "Approval or blocker note")
    checklist_errors = _domain_checklist_errors(section)
    required_value = _clean_value(required).lower()
    status_value = _clean_value(approval_status).lower()
    note_value = _clean_value(approval_note)

    # Documented docs-only / not-required branch. A PR that only *mentions* an evidence concept in
    # prose (a free-form trigger, e.g. a docs page discussing "benchmark interpretation") may take
    # the template's opt-out: `Required for this PR: no - reason` with `Status: not required`. A
    # structured Research Result Guidance declaration is a stronger self-declaration and stays on the
    # strict path below, so this cannot be used to wave through a genuinely evidence-sensitive PR.
    if _is_docs_only_domain_opt_out(sensitive_terms, required, status_value):
        return DomainApprovalReport(
            status="ok",
            source=source,
            sensitive_terms=sensitive_terms,
            required=required,
            approval_status=approval_status,
            approval_note=note_value,
            checklist_errors=(),
            message=(
                "Domain-aware approval is explicitly not required for this docs-only/support PR; "
                "the evidence term is a prose mention, not a structured evidence declaration."
            ),
        )

    if _is_empty_or_option_placeholder(required) or any(
        _has_value_prefix(required_value, prefix)
        for prefix in ("no", "n/a", "na", "not applicable")
    ):
        return DomainApprovalReport(
            status="domain_approval_required",
            source=source,
            sensitive_terms=sensitive_terms,
            required=required,
            approval_status=approval_status,
            approval_note=approval_note,
            checklist_errors=checklist_errors,
            message=(
                "Domain-sensitive evidence-validity terms are present; this PR must state "
                "that domain-aware approval is required or name the blocker."
            ),
        )
    if (
        _is_empty_or_option_placeholder(domains)
        or _is_empty_or_option_placeholder(approval_status)
        or _is_empty_or_option_placeholder(approval_note)
    ):
        return DomainApprovalReport(
            status="missing_domain_approval_note",
            source=source,
            sensitive_terms=sensitive_terms,
            required=required,
            approval_status=approval_status,
            approval_note=approval_note,
            checklist_errors=checklist_errors,
            message=(
                "Domain-aware approval requires reviewed domains, a non-empty status, "
                "and an approver/review source or waiver reason. Replace slash-separated "
                "template option text with concrete comma-separated domains, or a concrete "
                "NA reason when approval is not required."
            ),
        )
    if checklist_errors:
        return DomainApprovalReport(
            status="incomplete_domain_approval",
            source=source,
            sensitive_terms=sensitive_terms,
            required=required,
            approval_status=approval_status,
            approval_note=approval_note,
            checklist_errors=checklist_errors,
            message=(
                "Domain-aware approval requires completed validity-checklist fields: "
                f"{', '.join(checklist_errors)}."
            ),
        )
    status_report = _domain_status_report(
        status_value,
        source=source,
        sensitive_terms=sensitive_terms,
        required=required,
        approval_status=approval_status,
        approval_note=approval_note,
        checklist_errors=checklist_errors,
    )
    if status_report is not None:
        return status_report

    return DomainApprovalReport(
        status="ok",
        source=source,
        sensitive_terms=sensitive_terms,
        required=required,
        approval_status=approval_status,
        approval_note=note_value,
        checklist_errors=(),
        message="Domain-aware approval or waiver is explicit.",
    )


def analyze_body_quality(
    body: str,
    *,
    source: str,
    changed_files: tuple[str, ...] = (),
    require_substantive_body: bool = False,
) -> BodyQualityReport:
    """Return whether substantive PRs have a human-written body."""
    substantive_files = tuple(path for path in changed_files if _is_substantive_file(path))
    if not require_substantive_body:
        return BodyQualityReport(
            status="skipped",
            source=source,
            substantive_files=substantive_files,
            message="Substantive PR body check not requested.",
        )
    if not changed_files:
        return BodyQualityReport(
            status="skipped_no_changed_files",
            source=source,
            substantive_files=(),
            message="No changed-file list was provided for substantive PR body classification.",
        )
    if not substantive_files:
        return BodyQualityReport(
            status="ok",
            source=source,
            substantive_files=(),
            message="Changed files are documentation/template-only; human PR body gate is not required.",
        )
    if _is_bot_only_body(body):
        return BodyQualityReport(
            status="bot_only_body",
            source=source,
            substantive_files=substantive_files,
            message="Substantive source/configuration PRs cannot rely on bot-only release notes.",
        )
    if not _clean_body_for_quality(body):
        return BodyQualityReport(
            status="empty_body",
            source=source,
            substantive_files=substantive_files,
            message="Substantive source/configuration PRs require a non-empty human-written body.",
        )
    return BodyQualityReport(
        status="ok",
        source=source,
        substantive_files=substantive_files,
        message="Substantive changed files have a human PR body.",
    )


def _read_event_body(path: Path) -> str | None:
    payload = json.loads(path.read_text(encoding="utf-8"))
    pull_request = payload.get("pull_request")
    if isinstance(pull_request, dict) and isinstance(pull_request.get("body"), str):
        return pull_request["body"]
    issue = payload.get("issue")
    if isinstance(issue, dict) and isinstance(issue.get("body"), str):
        return issue["body"]
    return None


def _load_body(args: argparse.Namespace) -> tuple[str | None, str]:
    if args.body_file:
        path = args.body_file
        return path.read_text(encoding="utf-8"), str(path)

    event_path = args.github_event_path
    event_name = os.environ.get("GITHUB_EVENT_NAME", "")
    if event_path and (args.github_event_path or event_name == "pull_request"):
        path = Path(event_path)
        body = _read_event_body(path)
        if body is not None:
            return body, str(path)

    body_file = os.environ.get("PR_READY_PR_BODY_FILE")
    if body_file:
        path = Path(body_file)
        return path.read_text(encoding="utf-8"), str(path)

    env_event_path = os.environ.get("GITHUB_EVENT_PATH")
    if env_event_path and event_name == "pull_request":
        path = Path(env_event_path)
        body = _read_event_body(path)
        if body is not None:
            return body, str(path)

    return None, "none"


def _format_report(report: FollowupReport) -> str:
    issue_text = ", ".join(report.linked_issues) if report.linked_issues else "none"
    deferred = report.deferred_work if report.deferred_work else "none"
    no_issue = report.explicit_no_issue_reason if report.explicit_no_issue_reason else "none"
    state_errors = "; ".join(report.issue_state_errors) if report.issue_state_errors else "none"
    return (
        "PR follow-up check: "
        f"status={report.status}; source={report.source}; deferred={deferred!r}; "
        f"linked_issues={issue_text}; no_issue_reason={no_issue!r}; "
        f"issue_state_errors={state_errors!r}; {report.message}"
    )


def _format_domain_report(report: DomainApprovalReport) -> str:
    terms = ", ".join(report.sensitive_terms) if report.sensitive_terms else "none"
    required = report.required if report.required else "none"
    approval_status = report.approval_status if report.approval_status else "none"
    approval_note = report.approval_note if report.approval_note else "none"
    checklist_errors = ", ".join(report.checklist_errors) if report.checklist_errors else "none"
    return (
        "PR domain-approval check: "
        f"status={report.status}; source={report.source}; sensitive_terms={terms}; "
        f"required={required!r}; approval_status={approval_status!r}; "
        f"approval_note={approval_note!r}; checklist_errors={checklist_errors!r}; "
        f"{report.message}"
    )


def _format_body_quality_report(report: BodyQualityReport) -> str:
    files = ", ".join(report.substantive_files) if report.substantive_files else "none"
    return (
        "PR body-quality check: "
        f"status={report.status}; source={report.source}; substantive_files={files}; "
        f"{report.message}"
    )


def _build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--body-file", type=Path, help="Markdown PR body to check.")
    parser.add_argument(
        "--github-event-path",
        type=Path,
        help="GitHub event JSON containing pull_request.body.",
    )
    parser.add_argument("--json", action="store_true", help="Emit machine-readable JSON.")
    parser.add_argument(
        "--require-body",
        action="store_true",
        help="Fail closed when no PR body source is available.",
    )
    parser.add_argument(
        "--require-open-issues",
        action="store_true",
        help="Verify linked follow-up issues are open via REST helper (gh_issue_rest.py).",
    )
    parser.add_argument(
        "--changed-files-file",
        type=Path,
        help="Newline-delimited PR changed-file paths for substantive body classification.",
    )
    parser.add_argument(
        "--require-substantive-body",
        action="store_true",
        help="Fail empty or bot-only PR bodies when changed files include source/configuration.",
    )
    parser.add_argument(
        "--advisory",
        action="store_true",
        help=(
            "Advisory mode: report contract violations as warnings and exit 0 instead of failing. "
            "Findings still surface as GitHub Actions ::warning:: annotations on the PR."
        ),
    )
    return parser


def _emit_advisory_warning(message: str) -> None:
    """Emit a GitHub Actions warning annotation so advisory findings still surface on the PR."""
    print(f"::warning title=PR body contract (advisory)::{message}")


def _resolve_exit(failing_messages: list[str], *, advisory: bool) -> int:
    """Emit advisory warnings as needed and return the process exit code.

    Returns:
        int: 0 when there are no failures or when advisory mode downgrades them; 2 otherwise.
    """
    if not failing_messages:
        return 0
    if advisory:
        for message in failing_messages:
            _emit_advisory_warning(message)
        return 0
    return 2


def main(argv: list[str] | None = None) -> int:
    """Run the PR follow-up check CLI."""
    args = _build_parser().parse_args(argv)
    advisory = args.advisory or os.environ.get("PR_READY_ADVISORY", "").lower() in {
        "1",
        "true",
        "yes",
        "on",
    }
    body, source = _load_body(args)
    if body is None:
        report = FollowupReport(
            status="missing_body" if args.require_body else "skipped",
            source=source,
            deferred_work="",
            linked_issues=(),
            explicit_no_issue_reason="",
            issue_state_errors=(),
            message=(
                "No PR body source configured; provide --body-file, PR_READY_PR_BODY_FILE, "
                "or a pull_request GITHUB_EVENT_PATH."
            ),
        )
        if args.json:
            print(json.dumps(report.__dict__, sort_keys=True))
        else:
            print(_format_report(report))
        if args.require_body and report.status == "missing_body":
            if advisory:
                _emit_advisory_warning(report.message)
                return 0
            return 2
        return 0

    require_open = args.require_open_issues or os.environ.get(
        "PR_READY_REQUIRE_OPEN_FOLLOWUP_ISSUES", ""
    ).lower() in {"1", "true", "yes", "on"}
    changed_files = _read_changed_files(args.changed_files_file)
    require_substantive_body = args.require_substantive_body or os.environ.get(
        "PR_READY_REQUIRE_SUBSTANTIVE_BODY", ""
    ).lower() in {"1", "true", "yes", "on"}
    body_quality_report = analyze_body_quality(
        body,
        source=source,
        changed_files=changed_files,
        require_substantive_body=require_substantive_body,
    )
    report = analyze_body(body, source=source, require_open_issues=require_open)
    domain_report = analyze_domain_approval(body, source=source)
    failing_statuses = {
        "missing_followup",
        "missing_section",
        "issue_state_error",
        "residual_scope_without_followup",
    }
    domain_failing_statuses = {
        "missing_domain_approval",
        "domain_approval_required",
        "missing_domain_approval_note",
        "incomplete_domain_approval",
        "pending_domain_approval",
        "invalid_domain_approval_status",
    }
    body_quality_failing_statuses = {
        "empty_body",
        "bot_only_body",
    }
    if args.json:
        print(
            json.dumps(
                {
                    "body_quality": body_quality_report.__dict__,
                    "followups": report.__dict__,
                    "domain_approval": domain_report.__dict__,
                },
                sort_keys=True,
            )
        )
    else:
        stream = sys.stderr if report.status in failing_statuses else sys.stdout
        print(_format_report(report), file=stream)
        domain_stream = (
            sys.stderr if domain_report.status in domain_failing_statuses else sys.stdout
        )
        print(_format_domain_report(domain_report), file=domain_stream)
        quality_stream = (
            sys.stderr
            if body_quality_report.status in body_quality_failing_statuses
            else sys.stdout
        )
        print(_format_body_quality_report(body_quality_report), file=quality_stream)
    failing_messages = [
        rep.message
        for rep, statuses in (
            (report, failing_statuses),
            (domain_report, domain_failing_statuses),
            (body_quality_report, body_quality_failing_statuses),
        )
        if rep.status in statuses
    ]
    return _resolve_exit(failing_messages, advisory=advisory)


if __name__ == "__main__":
    raise SystemExit(main())
