"""Replay defensive PPO policy with observation adapters.

Usage:
    uv run python examples/advanced/09_defensive_policy.py

Prerequisites:
    - model/run_023

Expected Output:
    - Pygame window showing the defensive policy interacting with pedestrians.

Limitations:
    - Uses legacy Gym-trained checkpoints and direct `RobotEnv` instantiation.

References:
    - docs/dev_guide.md#baseline-policies
"""

import numpy as np
from gymnasium import spaces

from robot_sf.benchmark.helper_catalog import load_trained_policy
from robot_sf.gym_env.env_config import EnvSettings
from robot_sf.gym_env.robot_env import RobotEnv
from robot_sf.robot.differential_drive import DifferentialDriveSettings
from robot_sf.sensor.sensor_fusion import OBS_DRIVE_STATE, OBS_RAYS
from robot_sf.sim.sim_config import SimulationSettings


def run_simulation() -> None:
    """Execute the defensive policy rollout and render the result."""
    env_config = EnvSettings(
        sim_config=SimulationSettings(
            stack_steps=1,
            difficulty=0,
            ped_density_by_difficulty=[0.06],
        ),
        robot_config=DifferentialDriveSettings(radius=1.0),
    )
    env = RobotEnv(env_config, debug=True)
    env.observation_space, env.action_space = prepare_gym_spaces()
    model = load_trained_policy("./model/run_023")

    def obs_adapter(orig_obs):
        drive_state = orig_obs[OBS_DRIVE_STATE]
        ray_state = orig_obs[OBS_RAYS]
        drive_state = drive_state[:, :-1]
        drive_state[:, 2] *= 10
        drive_state = np.squeeze(drive_state)
        ray_state = np.squeeze(ray_state)
        return np.concatenate((ray_state, drive_state), axis=0)

    obs, _ = env.reset()
    for _ in range(10000):
        obs = obs_adapter(obs)
        action, _ = model.predict(obs, deterministic=True)
        obs, _, done, _, _ = env.step(action)
        env.render()

        if done:
            obs, _ = env.reset()
            env.render()
    env.exit()


